static uint32_t x8nmodp(uintmax_t n) {
    uint32_t xp = 0x80000000;
    int k = 3;
    for (;;) {
        if (n & 1)
            xp = multmodp(table_comb[k], xp);
        n >>= 1;
        if (n == 0)
            break;
        if (++k == 31)
            k = 0;
    }
    return xp;
}

uint32_t crc32iscsi_comb(uint32_t crc1, uint32_t crc2,
        uintmax_t len2) {
    return multmodp(x8nmodp(len2), crc1) ^ crc2;
}

