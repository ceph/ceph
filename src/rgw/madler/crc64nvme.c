uint64_t crc64nvme_byte(uint64_t crc, void const *mem, size_t len) {
    uint8_t const *data = mem;
    if (data == NULL)
        return 0;
    for (size_t i = 0; i < len; i++) {
        crc = (crc >> 8) ^
              table_byte[(crc ^ data[i]) & 0xff];
    }
    return crc;
}

static inline uint64_t swapmax(uint64_t crc) {
    return
        ((crc & 0xff) << 56) +
        ((crc & 0xff00) << 40) +
        ((crc & 0xff0000) << 24) +
        ((crc & 0xff000000) << 8) +
        ((crc & 0xff00000000) >> 8) +
        ((crc & 0xff0000000000) >> 24) +
        ((crc & 0xff000000000000) >> 40) +
        ((crc & 0xff00000000000000) >> 56);
}

// This code assumes that integers are stored big-endian.

uint64_t crc64nvme_word(uint64_t crc, void const *mem, size_t len) {
    uint8_t const *data = mem;
    if (data == NULL)
        return 0;
    while (len && ((ptrdiff_t)data & 0x7)) {
        len--;
        crc = (crc >> 8) ^
              table_byte[(crc ^ *data++) & 0xff];
    }
    uint64_t word = swapmax(crc);
    size_t n = len >> 3;
    for (size_t i = 0; i < n; i++) {
        word ^= ((uint64_t const *)data)[i];
        word = table_word[0][word & 0xff] ^
               table_word[1][(word >> 8) & 0xff] ^
               table_word[2][(word >> 16) & 0xff] ^
               table_word[3][(word >> 24) & 0xff] ^
               table_word[4][(word >> 32) & 0xff] ^
               table_word[5][(word >> 40) & 0xff] ^
               table_word[6][(word >> 48) & 0xff] ^
               table_word[7][word >> 56];
    };
    data += n << 3;
    len &= 7;
    crc = swapmax(word);
    while (len) {
        len--;
        crc = (crc >> 8) ^
              table_byte[(crc ^ *data++) & 0xff];
    }
    return crc;
}

static uint64_t multmodp(uint64_t a, uint64_t b) {
    uint64_t prod = 0;
    for (;;) {
        if (a & 0x8000000000000000) {
            prod ^= b;
            if ((a & 0x7fffffffffffffff) == 0)
                break;
        }
        a <<= 1;
        b = b & 1 ? (b >> 1) ^ 0x9a6c9329ac4bc9b5 : b >> 1;
    }
    return prod;
}

