static uint64_t x8nmodp(uintmax_t n) {
    uint64_t xp = 0x8000000000000000;
    int k = 3;
    for (;;) {
        if (n & 1)
            xp = multmodp(table_comb[k], xp);
        n >>= 1;
        if (n == 0)
            break;
        if (++k == 64)
            k = 0;
    }
    return xp;
}

