namespace {

// Run `argv` as a subprocess. Returns exit status and stdout.
// Used by default_invoker(); broken out so it's testable in
// isolation (and so the production path is the only fork-exec
// site).
DbusGaneshaSink::DbusResult run_subprocess(
    const std::vector<std::string>& argv) {
  if (argv.empty()) return {-1, ""};

  int pipefd[2];
  if (pipe(pipefd) != 0) return {-1, std::strerror(errno)};

  pid_t pid = fork();
  if (pid < 0) {
    close(pipefd[0]);
    close(pipefd[1]);
    return {-1, std::strerror(errno)};
  }

  if (pid == 0) {
    // child
    close(pipefd[0]);
    dup2(pipefd[1], STDOUT_FILENO);
    close(pipefd[1]);
    std::vector<char*> raw;
    raw.reserve(argv.size() + 1);
    for (const auto& s : argv) raw.push_back(const_cast<char*>(s.c_str()));
    raw.push_back(nullptr);
    execvp(raw[0], raw.data());
    _exit(127);
  }

  // parent
  close(pipefd[1]);
  std::string out;
  std::array<char, 4096> buf{};
  ssize_t n;
  while ((n = read(pipefd[0], buf.data(), buf.size())) > 0) {
    out.append(buf.data(), static_cast<size_t>(n));
  }
  close(pipefd[0]);
  int status = 0;
  waitpid(pid, &status, 0);
  int exit_code = WIFEXITED(status) ? WEXITSTATUS(status) : -1;
  return {exit_code, std::move(out)};
}

}  // namespace

DbusGaneshaSink::DbusInvoker DbusGaneshaSink::default_invoker() {
  return &run_subprocess;
}

